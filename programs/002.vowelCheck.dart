void main() {
  String ch = 'w';

  if (ch == 'A' ||
      ch == 'a' ||
      ch == 'E' ||
      ch == 'e' ||
      ch == 'I' ||
      ch == 'i' ||
      ch == 'O' ||
      ch == 'o' ||
      ch == 'U' ||
      ch == 'u') {
    print('$ch is vowel');
  } else {
    print('$ch is Consonant');
  }
}
